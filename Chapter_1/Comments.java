/**
 * File: Comments.java
 * Purpose: To demonstrate the three kinds of comments in Java.
 * Types:
 * 1. Single-line comment
 * 2. Multi-line comment
 * 3. Documentation comment
 */

public class Comments {
    public static void main(String[] args) {
        // Example of a single-line comment
        System.out.println("This line shows single-line comments usage.");

        /*
         Multi-line comments can explain a block of code
         or provide detailed notes for developers.
         This type of comment can cover several lines.
        */
        System.out.println("This line demonstrates multi-line comments.");

        /**
         * Documentation comments are often used with classes or methods.
         * They are especially helpful when generating API documentation.
         */
        System.out.println("This line shows documentation comments in action.");
    }
}
